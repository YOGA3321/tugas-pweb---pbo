            <h2>Konsep Website</h2>
            <p>Website yang akan dibuat mengambil topik <b>Restoran</b>. Fokus utama website ini adalah memberikan kemudahan dalam proses pemesanan, pengelolaan menu, dan pengaturan dapur. Selain itu, website restoran juga berfungsi sebagai media promosi, sehingga pelanggan bisa melihat menu, promo, dan layanan yang tersedia. Dengan adanya sistem ini, interaksi antara pelanggan, pelayan, dan dapur bisa lebih cepat dan terorganisir. Tujuan akhirnya adalah meningkatkan pengalaman pelanggan serta efisiensi operasional restoran.</p>
            
            <h3>Fitur & Halaman Utama</h3>
            <ol>
                <li><b>Home</b> – Halaman utama berisi pengenalan singkat website dan navigasi awal.</li>
                <li><b>Dashboard</b> – Menampilkan ringkasan data penting secara visual dan cepat.</li>
                <li><b>Menu</b> – Daftar menu makanan/minuman yang tersedia lengkap dengan detail.</li>
                <li><b>Kategori</b> – Pengelompokan menu berdasarkan jenis (contoh: makanan, minuman, dessert).</li>
                <li><b>Order</b> – Halaman untuk mencatat pesanan pelanggan secara keseluruhan.</li>
                <li><b>Order Item</b> – Detail dari tiap item pesanan pelanggan (jumlah, harga, catatan khusus).</li>
                <li><b>Report</b> – Laporan penjualan atau aktivitas dalam periode tertentu.</li>
                <li><b>Keuangan</b> – Sistem untuk mengelola data keuangan restoran, mencakup pendapatan harian, bulanan, hingga tahunan. Fitur ini membantu manajemen dalam memantau profit, mengidentifikasi tren penjualan, serta menyusun strategi bisnis.</li>
                <li><b>Pelayan</b> – Fitur yang membantu pelayan mengatur pesanan pelanggan.</li>
                <li><b>Dapur</b> – Tampilan untuk bagian dapur agar mengetahui pesanan yang harus disiapkan.</li>
            </ol>
            
            <hr>

            <h2>Rencana Desain</h2>
            <p>Website akan dirancang dengan konsep sederhana namun informatif, menggunakan warna netral dan aksen cerah untuk menonjolkan elemen penting. Navigasi utama diletakkan di bagian atas (navbar) agar mudah diakses, dengan struktur halaman yang konsisten. Setiap halaman akan dilengkapi dengan heading yang jelas, ikon pendukung, serta layout grid agar konten lebih teratur. Fokus utama desain adalah kemudahan penggunaan (user friendly) dan tampilan yang responsif di berbagai perangkat.</p>

            <h2>Resource yang Digunakan</h2>
            <ul>
                <li><b>Foto</b> – Digunakan untuk menampilkan ilustrasi menu makanan, suasana restoran, dan ikon navigasi.</li>
                <li><b>Video</b> – Dapat berupa video promosi, tutorial pemesanan online, atau dokumentasi suasana restoran.</li>
                <li><b>Deskripsi</b> – Berisi penjelasan detail dari tiap menu, informasi layanan, serta instruksi penggunaan fitur website.</li>
            </ul>

